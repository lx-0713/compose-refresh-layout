package com.lx.compose.refresh

/**
 * Created by lixiong on 2025/10/17.
 *
 * 刷新容器的生命周期状态枚举。
 * 
 * 定义了头部（Header）与尾部（Footer）在交互过程中的确定性状态，
 * 供手势拦截器与渲染层进行状态同步与事件分发。
 */
enum class SmartRefreshFlag {
    /**
     * 空闲状态。
     * 组件处于默认静止或内部子视图正常滚动阶段，不进行任何越界偏移渲染。
     */
    IDLE,

    /**
     * 越界拉拽状态。
     * 手势已越过物理边界并发生越界平移，但偏移量尚未达到触发回调的阈值。
     * 手势释放后将自动执行复位动画并流转回 [IDLE] 状态。
     */
    PULLING,

    /**
     * 异步执行状态。
     * 偏移量突破阈值并释放手势，已向业务层派发执行事件（onRefresh 或 onLoadMore）。
     * 组件停靠在指定的悬停边界进行反馈，等待业务层显式结束指令。
     */
    REFRESHING,

    /**
     * 闭幕回弹状态。
     * 业务层下发结束指令后触发，组件开始执行平滑的归位动画。
     * 该状态提供严格的互斥机制，确保归位动画的完整性，并在此期间屏蔽额外的交互输入。
     */
    FINISHING
}
